package com.ericsson.eniq.Services;



public class SanityCheckException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public SanityCheckException() {
	}

	public SanityCheckException(String arg0) {
		super(arg0);
	}

	public SanityCheckException(Throwable arg0) {
		super(arg0);
	}

	public SanityCheckException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

